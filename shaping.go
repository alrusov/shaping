package shaping

//----------------------------------------------------------------------------------------------------------------------------//

type (
	S struct {
		s chan bool
	}
)

//----------------------------------------------------------------------------------------------------------------------------//

func New(len int) (s *S) {
	s = &S{}

	if len <= 0 {
		return
	}

	s.s = make(chan bool, len)

	for i := 0; i < len; i++ {
		s.s <- true
	}

	return
}

//----------------------------------------------------------------------------------------------------------------------------//

func (s *S) In() {
	if s.s == nil {
		return
	}

	<-s.s
}

//----------------------------------------------------------------------------------------------------------------------------//

func (s *S) Out() {
	if s.s == nil {
		return
	}

	s.s <- true
}

